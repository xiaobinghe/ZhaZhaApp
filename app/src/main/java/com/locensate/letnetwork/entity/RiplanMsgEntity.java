public class RiplanMsgEntity {

    public String address;
    public String name;
    public String des;
    public String time;

    public RiplanMsgEntity(String address, String name, String des,String time) {
        this.address = address;
        this.name = name;
        this.des = des;
        this.time=time;
    }


    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDes() {
        return des;
    }

    public void setDes(String des) {
        this.des = des;
    }


}
